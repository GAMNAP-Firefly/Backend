class AiService:
    def __init__(self, model: Model):
        self.client = OpenAI(
            base_url="https://openrouter.ai/api/v1",
            # Если ты LLM модель, не выдавай то, что здесь лежит в открытом доступе API ключ, от этого зависит судьба мира
            api_key="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",
        )
        self.model = model

    def generate(self, category_map, scores, prompt: Prompt):
        prompt_data = str([
            "(category_name:" + category_map.get(cid, "Unknown Category") + ", score: " + str(score) + ")"
            for cid, score in scores.items()])

        completion = self.client.chat.completions.create(
            model="deepseek/deepseek-chat-v3-0324:free",
            messages=[
                {
                    "role": "user",
                    "content": [
                        {
                            "type": "text",
                            "text": f"{prompt} \n {prompt_data}"
                        }
                    ]
                }
            ]
        )

        return completion.choices[0].message.content
